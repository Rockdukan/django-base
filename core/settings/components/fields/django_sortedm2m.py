"""
Настройки django-sortedm2m для сортируемых отношений многие-ко-многим

Компонент отвечает за:
1. Настройку сортируемых связей многие-ко-многим
2. Конфигурацию отображения в администраторской панели
3. Настройки автоматической сортировки
4. Параметры сохранения порядка
"""


# ------------------ ОСНОВНЫЕ НАСТРОЙКИ SORTEDM2M -------------------

# Имя поля для сортировки
SORTEDM2M_SORT_FIELD_NAME = "sort_order"

# Включить автоматическую сортировку при сохранении
SORTEDM2M_AUTO_SORT = True

# ------------------ НАСТРОЙКИ ОТОБРАЖЕНИЯ В АДМИНКЕ -------------------

# Виджет по умолчанию для SortedM2M полей в админке
SORTEDM2M_ADMIN_WIDGET = "sortedm2m_fields.SortedMultipleChoiceField"

# Использовать виджет с фильтрами в админке
SORTEDM2M_ADMIN_USE_FILTER_WIDGET = True

# Количество элементов, отображаемых в админке
SORTEDM2M_ADMIN_DISPLAY_ITEMS = 15

# ------------------ НАСТРОЙКИ ПРОИЗВОДИТЕЛЬНОСТИ -------------------

# Использовать транзакции при сохранении порядка
SORTEDM2M_USE_TRANSACTIONS = True

# Использовать оптимизированные запросы для больших наборов данных
SORTEDM2M_OPTIMIZE_LARGE_DATASETS = True

# Пороговое значение для оптимизации запросов
SORTEDM2M_LARGE_DATASET_THRESHOLD = 1000

# ------------------ НАСТРОЙКИ ПОВЕДЕНИЯ -------------------

# Автоматически переназначать порядок при удалении элементов
SORTEDM2M_REORDER_ON_DELETE = True

# Автоматически назначать порядок для новых элементов
SORTEDM2M_ASSIGN_ORDER_TO_NEW_ITEMS = True

# Перемещать новые элементы в начало или конец списка
SORTEDM2M_NEW_ITEMS_TO_END = True

# ------------------ НАСТРОЙКИ КЭШИРОВАНИЯ -------------------

# Включить кэширование запросов сортировки
SORTEDM2M_CACHE_QUERIES = True

# Время жизни кэша (в секундах)
SORTEDM2M_CACHE_TIMEOUT = 60 * 5  # 5 минут

# Ключ кэша для запросов сортировки
SORTEDM2M_CACHE_KEY_PREFIX = "sortedm2m_order"

# ------------------ НАСТРОЙКИ МИГРАЦИЙ -------------------

# Управлять миграциями для полей SortedM2M
SORTEDM2M_MANAGE_MIGRATIONS = True

# ------------------ НАСТРОЙКИ ЛОГИРОВАНИЯ -------------------

# Включить логирование операций с сортировкой
SORTEDM2M_ENABLE_LOGGING = True

# Уровень логирования
SORTEDM2M_LOGGING_LEVEL = "INFO"

# Формат сообщений журнала
SORTEDM2M_LOGGING_FORMAT = "%(asctime)s - %(name)s - %(levelname)s - %(message)s"

# ------------------ НАСТРОЙКИ ИНТЕРФЕЙСА ПОЛЬЗОВАТЕЛЯ -------------------

# Включить возможность drag & drop сортировки в админке
SORTEDM2M_ENABLE_DRAG_AND_DROP = True

