SORTEDM2M_USE_AJAX_SORTING = True

# URL для AJAX запросов сортировки
SORTEDM2M_AJAX_SORT_URL = "sortedm2m/sort/"

# ------------------ НАСТРОЙКИ СОВМЕСТИМОСТИ -------------------

# Включить обратную совместимость со старыми версиями
SORTEDM2M_BACKWARDS_COMPATIBILITY = True
