"""
Настройки django-axes для защиты от попыток подбора пароля

Компонент отвечает за:
1. Настройку защиты от атак перебором
2. Параметры блокировки и разблокировки доступа
3. Настройки отслеживания попыток входа
4. Конфигурацию уведомлений о блокировке
"""


# ------------------ ОСНОВНЫЕ НАСТРОЙКИ AXES -------------------

# Включить защиту от перебора паролей
AXES_ENABLED = True

# ------------------ НАСТРОЙКИ БЛОКИРОВКИ -------------------

# Количество неудачных попыток входа до блокировки
AXES_FAILURE_LIMIT = 5

# Время блокировки в часах
AXES_COOLOFF_TIME = 1

# Сбросить счетчик неудачных попыток после успешного входа
AXES_RESET_ON_SUCCESS = True

# Заблокировать IP-адрес при превышении лимита неудачных попыток
AXES_LOCK_OUT_BY_COMBINATION_USER_AND_IP = True

# ------------------ НАСТРОЙКИ ОТСЛЕЖИВАНИЯ -------------------

# Отслеживать по IP-адресу
AXES_LOCK_OUT_AT_FAILURE = True

# Отслеживать по имени пользователя
AXES_ONLY_USER_FAILURES = False

# Отслеживать по IP-адресу и имени пользователя
AXES_USE_USER_AGENT = True

# Отслеживать неудачные попытки входа для несуществующих пользователей
AXES_LOCK_OUT_ANONYMOUS = True

# ------------------ НАСТРОЙКИ ХРАНЕНИЯ ДАННЫХ -------------------

# Метод хранения данных о попытках входа
# Поддерживаемые значения:
# "axes.backends.database.AxesDatabaseHandler" - хранение в базе данных
# "axes.backends.redis.AxesRedisHandler" - хранение в Redis
# "axes.backends.dummy.AxesDummyHandler" - подавление хранения (для тестирования)
AXES_HANDLER = "axes.backends.database.AxesDatabaseHandler"

# Время хранения данных о попытках входа в днях
AXES_COOLOFF_TIME = 1

# ------------------ НАСТРОЙКИ УВЕДОМЛЕНИЙ -------------------

# Включить уведомления администраторов о блокировках
AXES_SEND_ADMIN_NOTIFICATION = True

# Список email-адресов для уведомлений о блокировках
AXES_ADMIN_NOTIFICATION_RECIPIENTS = ["admin@example.com"]

# Тема письма с уведомлением о блокировке
AXES_ADMIN_NOTIFICATION_SUBJECT = "Блокировка доступа из-за превышения лимита попыток входа"

# ------------------ НАСТРОЙКИ БЕЛОГО СПИСКА -------------------

# IP-адреса, исключенные из проверок (белый список)
AXES_WHITELIST = []

# ------------------ НАСТРОЙКИ ЧЕРНОГО СПИСКА -------------------

# IP-адреса, всегда блокируемые (черный список)
AXES_BLACKLIST = []

# ------------------ НАСТРОЙКИ ПРОКСИ -------------------

# Включить поддержку проксированных запросов
AXES_BEHIND_REVERSE_PROXY = False

# Имя заголовка для получения IP-адреса клиента за прокси
AXES_PROXY_COUNT = 0

# Порядок заголовков для получения IP-адреса клиента
AXES_META_PRECEDENCE_ORDER = [
    "HTTP_X_FORWARDED_FOR",
    "REMOTE_ADDR",
]

# ------------------ НАСТРОЙКИ ПУТЕЙ -------------------

# Путь для формы входа
AXES_LOGIN_FORM_CLASS = "django.contrib.auth.forms.AuthenticationForm"

# URL-адрес для формы входа
AXES_LOGIN_URL = "/login/"

# URL-адрес перенаправления после блокировки
AXES_LOCKOUT_URL = "/locked/"

# Путь для проверки пароля
AXES_PASSWORD_FORM_FIELD = "password"

# Путь для проверки имени пользователя
AXES_USERNAME_FORM_FIELD = "username"

# ------------------ НАСТРОЙКИ ОТЛАДКИ -------------------

# Включить режим отладки
AXES_DEBUG = False

# ------------------ НАСТРОЙКИ ЛОГИРОВАНИЯ -------------------

# Включить логирование событий axes
AXES_ENABLE_LOGGING = True

# Уровень логирования
AXES_LOGGING_LEVEL = "INFO"

# Формат сообщений журнала
AXES_LOGGING_FORMAT = "%(asctime)s - %(name)s - %(levelname)s - %(message)s"
