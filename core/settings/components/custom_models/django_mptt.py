"""
Настройки django-mptt для работы с деревовидными структурами

Компонент отвечает за:
1. Настройку деревовидных моделей
2. Конфигурацию отображения дерева в админке
3. Параметры рекурсивных запросов
4. Настройки алгоритмов обработки деревьев
"""


# ------------------ ОСНОВНЫЕ НАСТРОЙКИ MPTT -------------------

# Имя поля уровня вложенности
MPTT_LEVEL_ATTR = "level"

# Имя поля для хранения левой границы узла
MPTT_LRANGE_ATTR = "lft"

# Имя поля для хранения правой границы узла
MPTT_RRANGE_ATTR = "rght"

# Имя поля для хранения дерева
MPTT_TREE_ATTR = "tree_id"

# Имя поля для хранения родительского элемента
MPTT_PARENT_ATTR = "parent"

# ------------------ НАСТРОЙКИ АВТОИНКРЕМЕНТА -------------------

# Шаг автоинкремента для полей границы
MPTT_AUTO_INCREMENT_STEP = 1

# Сохранять порядок в дереве при добавлении новых узлов
MPTT_KEEP_ORDER = True

# ------------------ НАСТРОЙКИ ОТОБРАЖЕНИЯ АДМИН-ПАНЕЛИ -------------------

# Использовать DraggableMPTTAdmin по умолчанию
MPTT_USE_DRAGGABLE_ADMIN = True

# Отступ для отображения вложенности в админке (в пикселях)
MPTT_ADMIN_LEVEL_INDENT = 10

# Обратный порядок отображения дерева в админке
MPTT_ADMIN_REVERSE_ORDER = False

# ------------------ НАСТРОЙКИ ПРОИЗВОДИТЕЛЬНОСТИ -------------------

# Использовать оптимизированные запросы для больших деревьев
MPTT_OPTIMIZE_LARGE_TREES = False

# Пороговое значение для оптимизации запросов
MPTT_LARGE_TREE_THRESHOLD = 1000

# Максимальное количество узлов для обработки за одну операцию
MPTT_MAX_NODES_PER_OPERATION = 500

# ------------------ НАСТРОЙКИ ПОВЕДЕНИЯ -------------------

# Включить проверку целостности дерева при сохранении
MPTT_CHECK_INTEGRITY = True

# Автоматическое восстановление дерева при обнаружении ошибок
MPTT_AUTO_FIX_TREE = True

# Использовать транзакции при изменении структуры дерева
MPTT_USE_TRANSACTIONS = True

# ------------------ НАСТРОЙКИ СОРТИРОВКИ -------------------

# Поля для сортировки узлов на одном уровне
MPTT_ORDERING_FIELD = "order"

# Автоматическая перенумерация порядка при изменении позиции
MPTT_AUTO_REORDER = True

# ------------------ НАСТРОЙКИ КЭШИРОВАНИЯ -------------------

# Включить кэширование запросов к дереву
MPTT_CACHE_QUERIES = True

# Время жизни кэша (в секундах)
MPTT_CACHE_TIMEOUT = 60 * 5  # 5 минут

# Ключ кэша для запросов дерева
MPTT_CACHE_KEY_PREFIX = "mptt_tree"

# ------------------ НАСТРОЙКИ МИГРАЦИЙ -------------------

# Управлять миграциями для полей MPTT
MPTT_MANAGE_MIGRATIONS = True

# ------------------ НАСТРОЙКИ ЛОГИРОВАНИЯ -------------------

# Включить логирование операций с деревом
MPTT_ENABLE_LOGGING = True

# Уровень логирования
MPTT_LOGGING_LEVEL = "INFO"

# Формат сообщений журнала
MPTT_LOGGING_FORMAT = "%(asctime)s - %(name)s - %(levelname)s - %(message)s"
