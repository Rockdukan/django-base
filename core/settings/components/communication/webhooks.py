import os

"""
Настройки вебхуков для Django

Компонент отвечает за:
1. Создание и обработку вебхуков (исходящих и входящих)
2. Настройку аутентификации и безопасности вебхуков
3. Конфигурацию ограничений и мониторинга вебхуков
4. Настройку обработки ответов
"""

# ------------------ ОСНОВНЫЕ НАСТРОЙКИ ВЕБХУКОВ -------------------

# Включить поддержку вебхуков
WEBHOOKS_ENABLED = False

# Базовый URL для вебхуков
WEBHOOKS_BASE_URL = ""

# ------------------ НАСТРОЙКИ БЕЗОПАСНОСТИ -------------------

# Включить подписывание вебхуков
WEBHOOKS_SIGNING_ENABLED = True

# Секретный ключ для подписи вебхуков (критическая информация)
WEBHOOKS_SIGNING_SECRET = os.environ.get("WEBHOOKS_SIGNING_SECRET", "")

# Алгоритм подписи вебхуков
WEBHOOKS_SIGNING_ALGORITHM = "hmac-sha256"

# Заголовок для подписи вебхуков
WEBHOOKS_SIGNATURE_HEADER = "X-Webhook-Signature"

# ------------------ НАСТРОЙКИ ИСХОДЯЩИХ ВЕБХУКОВ -------------------

# Таймаут для исходящих вебхуков (в секундах)
WEBHOOKS_OUTGOING_TIMEOUT = 5

# Максимальное количество повторных попыток для исходящих вебхуков
WEBHOOKS_OUTGOING_MAX_RETRIES = 3

# Стратегия повторных попыток
WEBHOOKS_OUTGOING_RETRY_STRATEGY = "exponential"

# Начальная задержка между повторными попытками (в секундах)
WEBHOOKS_OUTGOING_RETRY_DELAY = 60

# Асинхронная отправка вебхуков
WEBHOOKS_OUTGOING_ASYNC = True

# ------------------ НАСТРОЙКИ ВХОДЯЩИХ ВЕБХУКОВ -------------------

# URL-префикс для входящих вебхуков
WEBHOOKS_INCOMING_URL_PREFIX = "/api/webhooks/"

# Разрешенные IP-адреса для входящих вебхуков (пустой список = все разрешены)
WEBHOOKS_INCOMING_ALLOWED_IPS = []

# Автоматически генерировать вебхук URL с секретным ключом
WEBHOOKS_INCOMING_SECRET_IN_URL = True

# ------------------ НАСТРОЙКИ ОГРАНИЧЕНИЙ -------------------

# Ограничение на размер тела входящего вебхука (в байтах)
WEBHOOKS_INCOMING_MAX_SIZE = 1024 * 1024  # 1MB

# Ограничение скорости для входящих вебхуков (запросов в минуту)
WEBHOOKS_INCOMING_RATE_LIMIT = 60

# ------------------ НАСТРОЙКИ ЛОГИРОВАНИЯ -------------------

# Включить логирование вебхуков
WEBHOOKS_LOGGING_ENABLED = True

# Уровень логирования вебхуков
WEBHOOKS_LOGGING_LEVEL = "INFO"

# Хранить историю вебхуков в базе данных
WEBHOOKS_STORE_HISTORY = True

# Срок хранения истории вебхуков (в днях, 0 = бессрочно)
WEBHOOKS_HISTORY_RETENTION = 30

# ------------------ НАСТРОЙКИ ДЛЯ ТЕСТИРОВАНИЯ -------------------

# Режим тестирования (не отправляет реальные вебхуки)
WEBHOOKS_TEST_MODE = True

# URL для тестирования вебхуков
WEBHOOKS_TEST_URL = "https://webhook.site/"
